<h2>Our Credit Information Report would normally include the following:</h2>
<ul style="text-align:left; color:#6A019D;">
    <li>Registration Details (legal name ,addresses, registration no)</li>
    <li>Shareholder Information </li>
    <li>Director Information </li>
    <li>Capital Structure (nominal capital, authorized &amp; paid up capital)</li>
    <li>Shareholding Affiliations &amp; Other Business Relationships</li>
    <li>Business Activities of the subject company</li>
    <li>Commercial Information (e.g Export and Import details, Agencies)</li>
    <li>Financial Statement Information</li>
    <li>Description of premises and facilities (e.g property ownership)</li>
    <li>Other Assets (e.g. Vehicles, Ships)  </li>
    <li>Trade references (if available)</li>
    <li>Associated Banks, Auditors, Legal advisors and Legal Representatives </li>
    <li>Negative Information and Defaults (e.g payment records, bounced checks)</li>
    <li>Media Articles related to the subject (sourced from specialized databases)</li>
    <li>Desk Research and Internet Research using subscription business sites</li>
    <li>General Comments (additional information relevant to the subject)</li>
    <li>Credit Scoring Assessment</li>
</ul>